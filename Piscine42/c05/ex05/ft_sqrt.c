int	ft_sqrt(int nb)
{
	long	n;
	long	i;

	i = 0;
	n = nb;
	if (n < 1)
		return (0);
	if (n == 1)
		return (1);
	i = 2;
	if (n > 2)
	{
		while (i <= n / i)
		{
			if (i * i == n)
				return (i);
			i++;
		}
	}
	return (0);
}
