#include <stdlib.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;
	int	size;
	int	*result;

	size = max - min + 1;
	i = 0;
	result = malloc(sizeof(int *) * size);
	if (min >= max)
		return (0);
	if (range == NULL)
		return (-1);
	*range = result;
	i = 0;
	while (max > min)
	{
		result[i] = min;
		min++;
		i++;
	}
	return (i);
}
